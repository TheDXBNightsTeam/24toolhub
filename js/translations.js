// Helper function to get translation
function getTranslation(key, lang = 'en') {
  const keys = key.split('.');
  let translation = ToolTranslations[lang];
  
  for (const k of keys) {
    if (translation && translation[k]) {
      translation = translation[k];
    } else {
      return key; // Return key if translation not found
    }
  }
  
  return translation;
}

// Helper function to get tool-specific translation
function getToolTranslation(tool, key, lang = 'en') {
  if (ToolSpecificTranslations[tool] && 
      ToolSpecificTranslations[tool][lang] && 
      ToolSpecificTranslations[tool][lang][key]) {
    return ToolSpecificTranslations[tool][lang][key];
  }
  return key; // Return key if translation not found
}

